<?php

namespace App\Http\Controllers;

use App\Models\hospital;
use App\Models\report;
use Illuminate\Http\Request;

class reportController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //
        $data = report::all();
        return view('reports')->with('data',$data);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //
        $hospital = hospital::pluck('name','id');
        return view('addreport')->with('hospital',$hospital);
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $reports = new report();
        $reports->name =  $request->name;
        $reports->description =  $request->description;
        $reports->cost = $request->cost;
        $reports->hospital_id = $request->hospital;
        $reports->save();
        return redirect('rep');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $report = report::find($id);
        return view('reports')->with('reports',$report);
    }

